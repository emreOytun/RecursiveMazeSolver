package src;

public class WhiteStamp {
	int yPos;
	int xPos;
	
	public WhiteStamp(int yPos, int xPos) {
		super();
		this.yPos = yPos;
		this.xPos = xPos;
	}
	
	public int getyPos() {
		return yPos;
	}

	public void setyPos(int yPos) {
		this.yPos = yPos;
	}

	public int getxPos() {
		return xPos;
	}

	public void setxPos(int xPos) {
		this.xPos = xPos;
	}
	
}
